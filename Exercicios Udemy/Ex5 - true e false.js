function retornar(x){
    if(x==Boolean){
        if(x==false){
            return console.log(x = true);
        }

        else if(x==true){
            return console.log(x = false);
        }
    }

    if(x!=Boolean){
        if(x==1){
            return console.log(x = -1);
        }

        else if(x==-1){
            return console.log(x = 1);
        }
    }
}

retornar(-1);